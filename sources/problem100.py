# 21 (15/21)×(14/20)
# 120 (85/120)×(84/119)
# 697 493 (493/697)×(492/696)
# 4060 2871 (2871/4060)×(2870/4059)

for n in range(21, 10**4):
    for i in range(int(n / 2), n):
        p = (i / n) * ((i - 1) / (n - 1))
        if p > 0.49999999 and p < 0.50000001:
            print(n, i, p)
        if p > 0.500001:
            break
